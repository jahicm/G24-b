package ch.g24.api.models;

public class TimeRange {
    private String start;
    private String end;

    // getters & setters
    public String getStart() {
        return start;
    }

    public void setStart(String start) {
        this.start = start;
    }

    public String getEnd() {
        return end;
    }

    public void setEnd(String end) {
        this.end = end;
    }
}
